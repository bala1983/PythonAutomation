def filter_cars(car_dict: dict, search_criteria: tuple) -> dict:
    filtered_dict = {}
    for car_name, car_parameters in car_dict.items():
        try:
            car_year = car_parameters[1]
            car_engine = car_parameters[2]
            car_price = car_parameters[4]
        except IndexError as e:
            raise IndexError("Search criteria is invalid")

        if car_year >= search_criteria[0] and car_engine >= search_criteria[1] and car_price <= search_criteria[2]:
            filtered_dict[car_name] = car_parameters
    filtered_list = list(filtered_dict.items())
    five_only = filtered_list[0:5]
    sorted_list = sorted(five_only, key=lambda x: x[1][4])
    filtered_dict = dict(sorted_list)
    return filtered_dict

