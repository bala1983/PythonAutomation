def sum_all_numbers(numbers:list[str])->list:
    result_list = []
    for number in numbers:
        try:
            split_number = [int(x) for x in number.split(",")]
            result_list.append(sum(split_number))
            # print(sum(split_number))
        except ValueError as e:
            print(f"Не можу це зробити!: {e}")
            result_list.append("Не можу це зробити!")
        except AttributeError as e:
            print(f"Invalid input type: {e}")
    return result_list

